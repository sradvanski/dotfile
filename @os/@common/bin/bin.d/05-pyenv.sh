# @var string PYEMV_DIR - directory of pyenv
PYENV_DIR=${HOME}/.pyenv

# ==============================================================================
# PYENV SETUP
# ==============================================================================
if [[ -d ${PYENV_DIR} ]]; then

    # --------------------------------------------------------------------------
    # PYENV INIT
    # --------------------------------------------------------------------------
    if [[ $(command -v pyenv 1>/dev/null 2>&1) ]]; then
        eval "$(pyenv init -)"
    fi

    # --------------------------------------------------------------------------
    # PYENV-VIRTUALENV INIT
    # --------------------------------------------------------------------------
    if [[ $(command -v pyenv 1>/dev/null 2>&1) ]]; then
        eval "$(pyenv virtualenv-init -)"
    fi

fi
