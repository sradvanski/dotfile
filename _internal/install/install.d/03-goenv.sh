#
# ==============================================================================

# ==============================================================================
# VARIABLES
# ==============================================================================

# ------------------------------------------------------------------------------
# VARIABLES LOADING
# ------------------------------------------------------------------------------
source ${HOME}/.dotfiles/_internal/variables/variables.sh

# ------------------------------------------------------------------------------
# VARIABLES
# ------------------------------------------------------------------------------

# @const string CWD - current working directory
# @var string[] GO_VERSIONS - golang versions
GO_VERSIONS=(1.9.3)

# ------------------------------------------------------------------------------
# VARIABLES
# ------------------------------------------------------------------------------
export GOENV_ROOT="${DOT_HOME}/.goenv"
export PATH="${GOENV_ROOT}/bin:${GOENV_ROOT}/shims:$PATH"

# ==============================================================================
# EXECUTION
# ==============================================================================
if [[ $(command -v goenv) ]]; then

    # --------------------------------------------------------------------------
    # Install Versions of Pyenv
    # --------------------------------------------------------------------------
    for version in $GO_VERSIONS; do
        goenv install ${version}
    done

    # --------------------------------------------------------------------------
    # Rehash golang versions
    # --------------------------------------------------------------------------
    goenv rehash
    goenv global 1.9.3

fi

# ------------------------------------------------------------------------------
# CONFIGURE VM
# ------------------------------------------------------------------------------
unset GO_VERSIONS
