    # Install Versions of Pyenv
    # --------------------------------------------------------------------------
    for php in $PHP_LIST; do
        phpbrew install ${php}
    done

#fi

# ------------------------------------------------------------------------------
# CONFIGURE VM
# ------------------------------------------------------------------------------
unset PHP_LIST
