# ------------------------------------------------------------------------------
# VARIABLES LOADING
# ------------------------------------------------------------------------------
source ${HOME}/.dotfiles/_internal/variables/variables.sh

# ------------------------------------------------------------------------------
# VARIABLES
# ------------------------------------------------------------------------------

# @var string[] PHP_LIST - sdk list
PHP_LIST=(7.2 5.5)
PHPBREW_DIR=${DOT_HOME}/.phpbrew

# ==============================================================================
# PHPBREW SETUP
# ==============================================================================

# ------------------------------------------------------------------------------
# PHPBREW INITIALIZE
# ------------------------------------------------------------------------------
if [[ ! -d ${PHPBREW_DIR} ]]; then
    phpbrew init
fi

# ------------------------------------------------------------------------------
# PHPBREW ENABLE
# ------------------------------------------------------------------------------
if [[ -e ${PHPBREW_DIR}/bashrc ]]; then
    source ${PHPBREW_DIR}/bashrc
fi

# ==============================================================================
# EXECUTION
# ==============================================================================
#if [[ $(command -v phpbrew) ]]; then

