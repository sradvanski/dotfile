# ==============================================================================
# VARIABLES
# ==============================================================================

# ------------------------------------------------------------------------------
# VARIABLES LOADING
# ------------------------------------------------------------------------------
source ${HOME}/.dotfiles/_internal/variables/variables.sh

# ------------------------------------------------------------------------------
# VARIABLES
# ------------------------------------------------------------------------------

# @var string[] SDK_LIST - sdk list
SDK_LIST=(gradle maven)

# ==============================================================================
# SDKMAN SETUP
# ==============================================================================
if [[ -s "${HOME}/.sdkman/bin/sdkman-init.sh" ]]; then
    source "${HOME}/.sdkman/bin/sdkman-init.sh"
fi

# ==============================================================================
# EXECUTION
# ==============================================================================
if [[ $(command -v sdk) ]]; then

    # --------------------------------------------------------------------------
    # Install Versions of Pyenv
    # --------------------------------------------------------------------------
    for version in $SDK_LIST; do
        sdk install ${version}
    done

fi

# ------------------------------------------------------------------------------
# CONFIGURE VM
# ------------------------------------------------------------------------------
unset SDK_LIST
