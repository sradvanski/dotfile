#
# Notes:             Script used to install node versions
#
# Revision:          Last change: 01/2018 by SR :: node version installations
#
# ==============================================================================

# ==============================================================================
# VARIABLES
# ==============================================================================

# ------------------------------------------------------------------------------
# VARIABLES LOADING
# ------------------------------------------------------------------------------
source ${HOME}/.dotfiles/_internal/variables/variables.sh

# ------------------------------------------------------------------------------
# VARIABLES DEFINITION
# ------------------------------------------------------------------------------

# @var string[] NODE_VERSIONS - java versions
NODE_VERSIONS=("lts/carbon" "lts/argon" "lts/boron" "iojs")

# ==============================================================================
# SETUP
# ==============================================================================

# ------------------------------------------------------------------------------
# VARIABLES
# ------------------------------------------------------------------------------

# @var string NVM_DIR - source directory
export NVM_DIR="${DOT_HOME}/.nvm"

# -----------------------------------------------------------------------------
# PYENV INIT
# ------------------------------------------------------------------------------
[[ -s "${NVM_DIR}/nvm.sh" ]] && source "${NVM_DIR}/nvm.sh"

# ==============================================================================
# EXECUTION
# ==============================================================================
if [[ $(command -v nvm) ]]; then

    # --------------------------------------------------------------------------
    # Install Versions of Pyenv
    # --------------------------------------------------------------------------
    for version in $NODE_VERSIONS; do
        nvm install ${version}
    done

    # --------------------------------------------------------------------------
    # NVM Setup
    # --------------------------------------------------------------------------
    nvm use default

fi

# ------------------------------------------------------------------------------
# CONFIGURE VM
# ------------------------------------------------------------------------------
unset NODE_VERSIONS
