#!/usr/bin/env bash
# -*- Mode: sh -*-
# vi: set ft=sh :
#
#
# ==============================================================================
#
# Purpose:           Install System helpers
#
# Parameters:        (none) ;
#                    -----------------------------------------------------------
#                    (none) ;
#                    -----------------------------------------------------------
#
# Called From:       (script) sourced
#
# Author:            Samir Radvanski <samir.radvanski@gmail.com>
#
# Notes:             Script that uses installation of all required
#                    versions of system settings
#
# Revision:          Last change: 02/2018 by SR :: Helper Installation
#
# ==============================================================================
set -o errexit
set -o nounset
set -o pipefail
set -e

# ==============================================================================
# VARIABLES
# ==============================================================================

# ------------------------------------------------------------------------------
# VARIABLES LOADING
# ------------------------------------------------------------------------------
source ${HOME}/.dotfiles/_internal/variables/variables.sh

# ------------------------------------------------------------------------------
# VARIABLE DEFINITIONS
# ------------------------------------------------------------------------------

# @var string CWD - current working directory
CWD=${DOT_DOTFILES}/_internal/helpers

# ==============================================================================
# EXECUTION
# ==============================================================================

# ------------------------------------------------------------------------------
# INSTALL APPLICATIONS
# ------------------------------------------------------------------------------
for helper in ${CWD}/helpers.d/*; do

    if [[ -d ${helper} ]]; then
        bash ${helper}/install.sh
    else
        bash $helper
    fi

done

# ------------------------------------------------------------------------------
# UNREGISTER
# ------------------------------------------------------------------------------
unset CWD
