#!/usr/bin/env bash
# -*- Mode: sh -*-
# vi: set ft=sh :
#
#
# ==============================================================================
#
# Purpose:           Change shell
#
# Parameters:        (none) ;
#                    -----------------------------------------------------------
#                    (none) ;
#                    -----------------------------------------------------------
#
# Called From:       (script) sourced
#
# Author:            Samir Radvanski <samir.radvanski@gmail.com>
#
# Notes:             Script used to ensure that zsh is used
#
# Revision:          Last change: 02/2018 by SR :: ZSH setup
#
# ==============================================================================
set -o errexit
set -o nounset
set -o pipefail
set -e

# ==============================================================================
# VARIABLES
# ==============================================================================

# ------------------------------------------------------------------------------
# VARIABLES LOADING
# ------------------------------------------------------------------------------
source ${HOME}/.dotfiles/_internal/variables/variables.sh

# ------------------------------------------------------------------------------
# VARIABLE DEFINITIONS
# ------------------------------------------------------------------------------

# @var string CWD - current working directory
CWD=${DOT_DOTFILES}/_internal/helpers

# ==============================================================================
# EXECUTION
# ==============================================================================
if [[ $(command -v chsh) ]]; then

    # --------------------------------------------------------------------------
    # CHANGE USER SHELL
    # --------------------------------------------------------------------------
    if [[ ! ${SHELL} == "/bin/zsh" ]]; then
        sudo chsh -s /bin/zsh ${DOT_USER}
    fi

fi
